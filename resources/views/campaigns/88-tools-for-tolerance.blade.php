                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">11. Speak up when you hear slurs. Let
                                                                people know that&nbsp;prejudice is always unacceptable.
                                                            </font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">12. Imagine what your life might be
