                                                    <p><span style="font-size: 10pt; font-family: 'Trebuchet MS'">
                                                            <font style="font-size: 20px" color="#000000"><img
                                                                    src="frontend/images/stories/tiuk/green/Arrow1.jpg" alt=" "
                                                                    width="21" height="21">&nbsp;Tolerance - The debate
                                                            </font>
                                                        </span></p>

                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000"><strong>Toleration and
                                                                    Tolerance</strong> are terms used in </font><span
                                                                style="color: windowtext">social</span>
                                                            <font color="#000000">, </font><span
                                                                style="color: windowtext">cultural</span>
                                                            <font color="#000000"> and </font><span
                                                                style="color: windowtext">religious</span>
                                                            <font color="#000000"> contexts to describe attitudes and
                                                                practices that prohibit </font><span
                                                                style="color: windowtext">discrimination</span>
                                                            <font color="#000000"> against those whose practices or
                                                                group memberships may be disapproved of by those in the
                                                                majority. Though developed to refer to the </font><span
                                                                style="color: windowtext">religious toleration</span>
                                                            <font color="#000000"> of minority religious sects following
                                                                the </font><span style="color: windowtext">Protestant
                                                                Reformation</span>
                                                            <font color="#000000">, these terms are increasingly used to
                                                                refer to a wider range of tolerated practices and
                                                                groups, such as the toleration of sexual practices and
                                                                orientations, or of political parties or ideas widely
                                                                considered objectionable.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">The principle of toleration is
                                                                controversial. </font><span
                                                                style="color: windowtext">Liberal</span>
                                                            <font color="#000000"> critics may see in it an
                                                                inappropriate implication that the "tolerated" custom or
                                                                behavior is an </font><span
                                                                style="color: windowtext">aberration</span>
                                                            <font color="#000000"> or that authorities have a right to
                                                            </font><span style="color: windowtext">punish</span>
                                                            <font color="#000000"> difference; such critics may instead
                                                                emphasize notions such as </font><span
                                                                style="color: windowtext">civility</span>
                                                            <font color="#000000"> or </font><span
                                                                style="color: windowtext">pluralism</span>
                                                            <font color="#000000">. Other critics, some sympathetic to
                                                                traditional </font><span
                                                                style="color: windowtext">fundamentalism</span>
                                                            <font color="#000000">, condemn toleration as a form of
                                                            </font><span style="color: windowtext">moral
                                                                relativism</span>
                                                            <font color="#000000">. On the other hand, defenders of
                                                                toleration may define it as involving positive regard
                                                                for difference or, alternately, may regard a narrow
                                                                definition of the term as more specific and useful than
                                                                its proposed alternatives, since it does not require
                                                                false expression of enthusiasm for groups or practices
                                                                that are genuinely disapproved of.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">As a practical matter, governments
                                                                have always had to consider the question of which groups
                                                                and practices to tolerate and which to persecute. The
                                                                expanding </font><span style="color: windowtext">Roman
                                                                Empire</span>
                                                            <font color="#000000">, for example faced the question of
                                                                whether or to what extent they should permit or
                                                                persecute the local beliefs and practices of groups
                                                                inhabiting annexed territories. </font><span
                                                                style="color: windowtext">Jewish</span>
                                                            <font color="#000000"> or </font><span
                                                                style="color: windowtext">Christian</span>
                                                            <font color="#000000"> practices or beliefs could be
                                                                tolerated or vigorously persecuted. Likewise, during the
                                                                Middle Ages, the rulers of Christian Europe or the
                                                                Muslim Middle East sometimes extended toleration to
                                                                minority religious groups, and sometimes did not. Jews
                                                                in particular suffered under </font><span
                                                                style="color: windowtext">anti-semitic</span>
                                                            <font color="#000000"> persecutions during the medieval
                                                                period.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000"><strong>The Enlightenment</strong>
                                                            </font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">However the development of a body of
                                                                theory on the subject of "toleration" begins in </font>
                                                            <span style="color: windowtext">Europe</span>
                                                            <font color="#000000"> during the sixteenth and seventeenth
                                                                centuries, in response to the </font><span
                                                                style="color: windowtext">Protestant Reformation</span>
                                                            <font color="#000000"> and the </font><span
                                                                style="color: windowtext">Wars of Religion</span>
                                                            <font color="#000000"> and persecutions that followed the
                                                                breaks with the </font><span
                                                                style="color: windowtext">Catholic Church</span>
                                                            <font color="#000000"> instigated by </font><span
                                                                style="color: windowtext">Martin Luther</span>
                                                            <font color="#000000"> and </font><span
                                                                style="color: windowtext">Huldrych Zwingli</span>
                                                            <font color="#000000"> and others. In response to the theory
                                                                of persecution that was used to justify wars of religion
                                                                and the execution of persons convicted of </font><span
                                                                style="color: windowtext">heresy</span>
                                                            <font color="#000000"> and </font><span
                                                                style="color: windowtext">witchcraft</span>
                                                            <font color="#000000">, writers such as </font><span
                                                                style="color: windowtext">Sebastian Castellio</span>
                                                            <font color="#000000"> and </font><span
                                                                style="color: windowtext">Michel de Montaigne</span>
                                                            <font color="#000000"> questioned the morality of religious
                                                                persecution, and offered arguments for toleration.
                                                            </font>
                                                        </span></p>
                                                    <p>
                                                        <font color="#000000"><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">A
                                                                detailed and influential body of writing on the question
                                                                of toleration however, was first produced in
                                                            </span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">Britain</span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                                in the Seventeenth Century, during and after the
                                                                destructive <span style="color: windowtext">English
                                                                    Civil Wars</span>. <span
                                                                    style="color: windowtext">John Milton</span> and
                                                                radical Parliamentarians such as <span
                                                                    style="color: windowtext">Gerrard Winstanley</span>
                                                                argued that Christian and Jewish worship should be
                                                                protected, and it was during the period that <span
                                                                    style="color: windowtext">Oliver Cromwell</span>
                                                                allowed the return of Jews to </span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">England</span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">.
                                                                These early theories of toleration were limited however,
                                                                and did not extend toleration to <span
                                                                    style="color: windowtext">Roman Catholics</span>
                                                                (who were perceived as disloyal to their country) or
                                                                <span style="color: windowtext">atheists</span> (who
                                                                were held to lack any moral basis for action). <span
                                                                    style="color: windowtext">John Locke</span>, in his
                                                                <span style="color: windowtext">Letter Concerning
                                                                    Toleration</span> and <span
                                                                    style="color: windowtext">Two Treatises of
                                                                    Government</span> proposed a more detailed and
                                                                systematic theory of toleration, which included a
                                                                principle of <span style="color: windowtext">Separation
                                                                    of Church and State</span> that formed the basis for
                                                                future constitutional democracies. The British <span
                                                                    style="color: windowtext">Toleration Act of
                                                                    1689</span> was the political result of seventeenth
                                                                century theorists and political exigency, which despite
                                                                the limited scope of the toleration it granted was
                                                                nevertheless a key development in the history of
                                                                toleration, which helped produce greater political
                                                                stability in the </span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">British
                                                                Isles</span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">.</span>
                                                        </font>
                                                    </p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">The philosophers and writers of the
                                                            </font><span style="color: windowtext">Enlightenment</span>
                                                            <font color="#000000">, especially </font><span
                                                                style="color: windowtext">Voltaire</span>
                                                            <font color="#000000"> and </font><span
                                                                style="color: windowtext">Lessing</span>
                                                            <font color="#000000">, promoted and further developed the
                                                                notion of religious tolerance, which however was not
                                                                sufficient to prevent the atrocities of the </font><span
                                                                style="color: windowtext">Reign of Terror</span>
                                                            <font color="#000000">. The incorporation by </font><span
                                                                style="color: windowtext">Thomas Jefferson</span>
                                                            <font color="#000000"> and others of Locke's theories of
                                                                toleration into the </font><span
                                                                style="color: windowtext">Constitution of the United
                                                                States of America</span>
                                                            <font color="#000000"> was arguably more successful.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'"><span
                                                                style="font-size: 10pt; color: black; line-height: 150%; font-family: 'Trebuchet MS'"><strong>Religion
                                                                    and Tolerance</strong></span></span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'"><span
                                                                style="font-size: 10pt; color: black; line-height: 150%; font-family: 'Trebuchet MS'">Though
                                                                developed to refer to the </span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                                <font color="#000000">religious toleration<span
                                                                        style="color: black"> of minority religious
                                                                        sects following the </span>Protestant
                                                                    Reformation<span style="color: black">, the terms
                                                                        "toleration" and "tolerance" are increasingly
                                                                        used to refer to a wider range of tolerated
                                                                        practices and groups, such as the toleration of
                                                                        sexual practices and orientations, or of
                                                                        political parties or ideas widely considered
                                                                        objectionable. Changing applications and
                                                                        understandings of the term can sometimes make
                                                                        debate on the question difficult.</span></font>
                                                            </span></span> </p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">For example, a distinction is
                                                                sometimes drawn between mere "Toleration" and a higher
                                                                notion of "Religious Liberty":</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">Some philosophers [. . .] regard
                                                                toleration and religious freedom as quite distinct
                                                                things and emphasize the differences between the two.
                                                                They understand toleration to signify no more than
                                                                forbearance and the permission given by the adherents of
                                                                a dominant religion for other religions to exist, even
                                                                though the latter are looked upon with disapproval as
                                                                inferior, mistaken, or harmful. In contrast these
                                                                thinkers recognize religious liberty as as the
                                                                recognition of equal freedom for all religions and
                                                                denominations without any kind of discrimination among
                                                                them [. . .] in the case of religious liberty, no one is
                                                                rightfully possessed of the power not to tolerate or to
                                                                cancel this liberty.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">Discussions of toleration therefore
                                                                often divided between those who view the term as a
                                                                minimal and perhaps even historical virtue (perhaps
                                                                today to be replaced by a more positive and robust
                                                                appreciation of </font><span
                                                                style="color: windowtext">pluralism</span>
                                                            <font color="#000000"> or </font><span
                                                                style="color: windowtext">diversity</span>
                                                            <font color="#000000">), and those who view it as a concept
                                                                with an important continuing vitality, and who are more
                                                                likely to use the term in considering contemporary
                                                                issues regarding discrimination on the basis of race,
                                                                nationality, gender, sexuality, disability, and other
                                                                reasons.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">There are also debates with regard to
                                                                the historical factors that produced the principle of
                                                                toleration, as well as to the proper reasons toleration
                                                                should be exercised, with some arguing that the growth
                                                                of </font><span
                                                                style="color: windowtext">skepticism</span>
                                                            <font color="#000000"> was an important or necessary factor
                                                                in the development of toleration, and others arguing
                                                                that religious belief or an evolving notion of respect
                                                                for individual persons was or is the basis on which
                                                                toleration was or should be practiced.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000"><strong>Tolerance and
                                                                    Monotheism</strong></font>
                                                        </span><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">&nbsp;</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'"><span
                                                                style="color: windowtext">One theory of the origins of
                                                                religious intolerance, propounded by </span><span
                                                                style="color: windowtext">Sigmund Freud</span><span
                                                                style="color: windowtext"> in </span><span
                                                                style="color: windowtext">Moses and Monotheism, links
                                                                intolerance to monotheism. More recently, Bernard Lewis
                                                                and Mark Cohen have argued that the modern understanding
                                                                of tolerance, involving concepts of national identity
                                                                and equal citizenship for persons of different
                                                                religions, was not considered a value by pre-modern
                                                                Muslims or Christians, due to the implications of
                                                                monotheism.</span><span style="color: windowtext"> The
                                                                historian </span><span style="color: windowtext">G.R.
                                                                Elton</span><span style="color: windowtext"> explains
                                                                that in pre-modern times, monotheists viewed such
                                                                toleration as a sign of weakness or even wickedness
                                                                towards God. </span><span style="color: windowtext">The
                                                                usual definition of tolerance in pre-modern times as
                                                            </span><span style="color: windowtext">Bernard
                                                                Lewis</span><span style="color: windowtext"> puts it was
                                                                that:</span></span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'"><span
                                                                style="color: windowtext">I am in charge. I will allow
                                                                you some though not all of the rights and privileges
                                                                that I enjoy, provided that you behave yourself
                                                                according to rules that I will lay down and
                                                                enforce.</span></span><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">&nbsp;</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'"><span
                                                                style="color: windowtext">Mark Cohen</span>
                                                            <font color="#000000"> states that it seems that all the
                                                                monotheistic religions in power throughout the history
                                                                have felt it proper, if not obligatory, to persecute
                                                                nonconforming religions. Therefore, Cohen concludes,
                                                                Medieval Islam and Medieval Christianity in power should
                                                                have persecuted non-believers in their lands and
                                                                "Judaism, briefly in power during the Hasmonean period
                                                                (second</font>
                                                        </span><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">century BCE) should have persecuted
                                                                pagan Idumeans". Cohen continues: "When all is said and
                                                                done, however, the historical evidence indicates that
                                                                the Jews of Islam, especially during the formative and
                                                                classical centuries (up to thirteenth century),
                                                                experienced much less persecution than did the Jews of
                                                                Christendom. This begs a more thorough and nuanced
                                                                explanation than has hitherto been given.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">Philosopher '</font><span
                                                                style="color: windowtext">Karl Popper</span>
                                                            <font color="#000000">s assertion in </font><span
                                                                style="color: windowtext">The Open Society and Its
                                                                Enemies</span>
                                                            <font color="#000000"> that we are warranted in refusing to
                                                                tolerate intolerance illustrates that there are limits
                                                                to tolerance.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">In particular, should a tolerant
                                                                society tolerate intolerance? What if by tolerating
                                                                action "A", society destroys itself? Tolerance of "A"
                                                                could be used to introduce a new thought system leading
                                                                to intolerance of vital institution "B". It is difficult
                                                                to strike a balance and different societies do not
                                                                always agree on the details, indeed different groups
                                                                within a single society also often fail to agree. The
                                                                current suppression of </font><span
                                                                style="color: windowtext">Nazism</span>
                                                            <font color="#000000"> in </font><span
                                                                style="color: windowtext">Germany</span>
                                                            <font color="#000000"> is considered intolerant by some
                                                                countries, for instance, while in </font>
                                                        </span>
                                                        <font color="#000000"><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">Germany</span><span
                                                                style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                                itself it is Nazism which is considered intolerably
                                                                intolerant.</span></font>
                                                    </p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">Philosopher </font><span
                                                                style="color: windowtext">John Rawls</span>
                                                            <font color="#000000"> devotes a section of his influential
                                                                and controversial book </font><span
                                                                style="color: windowtext">A Theory of Justice</span>
                                                            <font color="#000000"> to the problem of whether a just
                                                                society should or should not tolerate the intolerant,
                                                                and to the related problem of whether or not, in any
                                                                society, the intolerant have any right to complain when
                                                                they are not tolerated.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">Rawls concludes that a just society
                                                                must be tolerant; therefore, the intolerant must be
                                                                tolerated, for otherwise, the society would then be
                                                                intolerant, and so unjust. However, Rawls qualifies this
                                                                by insisting that society and its social institutions
                                                                have a reasonable right of self-preservation that
                                                                supersedes the principle of tolerance. Hence, the
                                                                intolerant must be tolerated but only insofar as they do
                                                                not endanger the tolerant society and its institutions.
                                                            </font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">Similarly, continues Rawls, while the
                                                                intolerant might forfeit the right to complain when they
                                                                are themselves not tolerated, other members of society
                                                                have a right, perhaps even a duty, to complain on their
                                                                behalf, again, as long as society itself is not
                                                                endangered by these intolerant members. The </font><span
                                                                style="color: windowtext">ACLU</span>
                                                            <font color="#000000"> is a good example of a social
                                                                institution that protects the rights of the intolerant,
                                                                as it frequently defends the right to free speech of
                                                                such intolerant organizations as the </font><span
                                                                style="color: windowtext">Ku Klux Klan</span>
                                                            <font color="#000000">.</font>
                                                        </span></p>
                                                    <p><span
                                                            style="font-size: 10pt; line-height: 150%; font-family: 'Trebuchet MS'">
                                                            <font color="#000000">Followers of Ayn Rand tend to see
                                                                tolerance as associated with the institution of
                                                                objective law. Attempts to increase tolerance by
                                                                applying different rules to different people would
                                                                ultimately be self defeating.</font>
                                                        </span></p>
